package package2;

public class String1 {

	public static void main(String[] args) {
		String name="Soumya";
		System.out.println("Length of the string is "+name.length());
		System.out.println("Character at index 1 of the string is "+name.charAt(1));
		System.out.println("Index of the character M of the string is "+name.indexOf('m'));
		System.out.println("String in lowercase"+name.toLowerCase());
		System.out.println("String in uppercase"+name.toUpperCase());
	}

}
